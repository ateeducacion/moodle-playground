#!/usr/bin/env node

import { execFileSync } from "node:child_process";
import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const repoDir = resolve(scriptDir, "..");
const jsOutputPath = resolve(repoDir, "src/generated/build-version.js");
const jsonOutputPath = resolve(repoDir, "assets/build-version.json");

function tryExec(args) {
  try {
    return execFileSync("git", args, {
      cwd: repoDir,
      encoding: "utf8",
      stdio: ["ignore", "pipe", "ignore"],
    }).trim();
  } catch {
    return "";
  }
}

function formatUtcTimestamp(date) {
  const year = String(date.getUTCFullYear()).padStart(4, "0");
  const month = String(date.getUTCMonth() + 1).padStart(2, "0");
  const day = String(date.getUTCDate()).padStart(2, "0");
  const hours = String(date.getUTCHours()).padStart(2, "0");
  const minutes = String(date.getUTCMinutes()).padStart(2, "0");
  const seconds = String(date.getUTCSeconds()).padStart(2, "0");
  return `${year}${month}${day}T${hours}${minutes}${seconds}Z`;
}

function getGitMetadata() {
  const gitSha = tryExec(["rev-parse", "--short=12", "HEAD"]) || "nogit";
  const dirty = Boolean(tryExec(["status", "--porcelain"]));
  return { gitSha, dirty };
}

const generatedAt = new Date().toISOString();
const { gitSha, dirty } = getGitMetadata();
const buildVersion =
  process.env.BUILD_VERSION ||
  `${formatUtcTimestamp(new Date())}-${gitSha}${dirty ? "-dirty" : ""}`;

const metadata = {
  buildVersion,
  generatedAt,
  gitSha,
  dirty,
};

mkdirSync(dirname(jsOutputPath), { recursive: true });
mkdirSync(dirname(jsonOutputPath), { recursive: true });

writeFileSync(
  jsOutputPath,
  [
    "// Generated by scripts/write-build-version.mjs",
    `export const BUILD_VERSION = ${JSON.stringify(buildVersion)};`,
    "export const BUILD_METADATA = {",
    `  buildVersion: ${JSON.stringify(metadata.buildVersion)},`,
    `  generatedAt: ${JSON.stringify(metadata.generatedAt)},`,
    `  gitSha: ${JSON.stringify(metadata.gitSha)},`,
    `  dirty: ${JSON.stringify(metadata.dirty)},`,
    "};",
    "",
  ].join("\n"),
  "utf8",
);

writeFileSync(jsonOutputPath, `${JSON.stringify(metadata, null, 2)}\n`, "utf8");

console.log(`Wrote build metadata ${buildVersion}`);
